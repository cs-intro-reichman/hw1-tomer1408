// Prints the numbers 0 to 5.
// (Same program as shown in lecture 1-1).
public class PrintTo5 {
    public static void main(String[] args) {
  
        int i = 0;
      
        while (i < 6) {
       
            System.out.println(i);
            i = i + 1;
        }
        System.out.println("Done");
    }
}